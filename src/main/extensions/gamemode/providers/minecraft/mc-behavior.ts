      leisureActions.push({ kind: 'chat', message: ['澶╀寒浜嗭紒鍙堟椿杩囦竴鏅氾綖', '鏃╁畨锛佹柊鐨勪竴澶╋紒', '澶槼鍑烘潵浜嗏€︹€﹀ソ鏆栧拰'][Math.floor(Math.random() * 3)] })
